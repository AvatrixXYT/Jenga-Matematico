package com.avatrixxyt.logic;

public class Operaciones {
    public String Suma (int Num1, int Num2){
        return (Num1 + " + " + Num2);
    }
    public String Resta (int Num1, int Num2){
        return (Num1 + " - " + Num2);
    }
    public String Multi (int Num1, int Num2){
        return (Num1 + " * " + Num2);
    }
    public String Div (int Num1, int Num2){
        return (Num1 + " / " + Num2);
    }
}
